""" ------------------------------

    String Indices

------------------------------ """

mystr = "Marguerite"

# only the indexed letter
print(mystr[0])

# up to, but not including, the specified letter index
print(mystr[:3])

# the indexed letter through the rest of the string
print(mystr[3:])

# including the 1st index up to
# but not including the 2nd index
print(mystr[1:3])
